class calculator{
  
    constructor(num1,num2)
    {
        num1=0;
        num2=0;
    }
    add(num1,num2)
    {
        return num1+num2;
    }
    subtract(num1,num2)
    {
        return num1-num2;
    }
    mult(num1,num2)
    {
        return num1*num2;
    }
    div(num1,num2)
    {
        return num1*num2;
    }
}
export {calculator}